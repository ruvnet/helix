//! # helix-refranges — population reference intervals (NHANES-derived FALLBACK)
//!
//! Imported values (especially from OCR or CSV) often arrive with **no reference
//! range**, so the focus/trend logic can't tell whether a value is out of range.
//! This crate provides a **labeled fallback**: per-analyte reference intervals
//! derived from **NHANES 2021–2023** (CDC, public domain) — the 2.5th–97.5th
//! percentiles over ~5,600–6,000 U.S. adults.
//!
//! ⚠️ **These are POPULATION percentiles, not lab-specific clinical reference
//! intervals.** Because NHANES is a general population (including people with
//! disease), some intervals are wider than a healthy-cohort clinical range (e.g.
//! glucose/triglycerides/CRP). They are an honest *fallback for orientation*, must
//! be labeled as such in any UI (ADR-006 evidence tiering), and never override a
//! lab's own range when one is supplied. Regenerate:
//! `cargo run -p helix-bioage --example nhanes_refranges`.

use helix_provenance::{RangePosition, ReferenceRange};

/// One population reference interval, keyed by LOINC code.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct RefInterval {
    pub loinc: &'static str,
    pub name: &'static str,
    pub unit: &'static str,
    /// 2.5th percentile (population low).
    pub low: f64,
    /// 97.5th percentile (population high).
    pub high: f64,
    /// Population median (50th percentile).
    pub median: f64,
}

const fn r(
    loinc: &'static str,
    name: &'static str,
    unit: &'static str,
    low: f64,
    high: f64,
    median: f64,
) -> RefInterval {
    RefInterval {
        loinc,
        name,
        unit,
        low,
        high,
        median,
    }
}


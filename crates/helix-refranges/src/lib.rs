/// Look up the population reference interval for a LOINC code.
pub fn lookup(loinc: &str) -> Option<&'static RefInterval> {
    NHANES_REF.iter().find(|r| r.loinc == loinc)
}

/// Build a [`ReferenceRange`] fallback for a LOINC code (for records that arrived
/// without one). Returns `None` if the code isn't covered.
pub fn reference_range_for(loinc: &str) -> Option<ReferenceRange> {
    lookup(loinc).map(|r| ReferenceRange::new(Some(r.low), Some(r.high)))
}

/// Classify a value against the population interval for a code (fallback only).
pub fn position_for(loinc: &str, value: f64) -> Option<RangePosition> {
    lookup(loinc).map(|r| {
        if value < r.low {
            RangePosition::Below
        } else if value > r.high {
            RangePosition::Above
        } else {
            RangePosition::Within
        }
    })
}

/// How many analytes the fallback table covers.
pub fn coverage() -> usize {
    NHANES_REF.len()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn covers_common_markers_with_sane_intervals() {
        assert!(coverage() >= 25);
        let cr = lookup("2160-0").unwrap(); // creatinine
        assert_eq!(cr.name, "Creatinine");
        assert!(cr.low < cr.median && cr.median < cr.high);
        assert!((0.4..0.7).contains(&cr.low) && (1.2..1.6).contains(&cr.high));
    }

    #[test]
    fn builds_reference_range_fallback() {
        let rr = reference_range_for("2951-2").unwrap(); // sodium 134–144
        assert_eq!(rr.low, Some(134.0));
        assert_eq!(rr.high, Some(144.0));
        assert!(reference_range_for("0000-0").is_none());
    }

    #[test]
    fn classifies_against_population_interval() {
        assert_eq!(position_for("2160-0", 0.3), Some(RangePosition::Below)); // low creatinine
        assert_eq!(position_for("2160-0", 0.9), Some(RangePosition::Within));
        assert_eq!(position_for("2160-0", 2.0), Some(RangePosition::Above));
        assert_eq!(position_for("nope", 1.0), None);
    }

    #[test]
    fn every_interval_is_ordered_and_labeled() {
        for ri in NHANES_REF {
            assert!(ri.low < ri.median && ri.median <= ri.high, "{}", ri.name);
            assert!(!ri.loinc.is_empty() && !ri.unit.is_empty());
        }
        assert!(SOURCE.contains("NHANES"));
    }
}
